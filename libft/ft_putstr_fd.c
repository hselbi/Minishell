#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	j;

	i = 0;
	if (!s)
		return ;
	j = ft_strlen(s);
	if (s)
	{
		while (s[i] && j > i)
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
