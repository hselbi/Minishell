#include "stdio.h"

char	*ft_strrchr(const char *s, int c)
{
	char	a;
	int		i;

	i = 0;
	a = (char) c;
	while (s[i])
		i++;
	while ((char)s[i] != a)
	{
		if (i == 0)
			return (0);
		i--;
	}
	return ((char *)(s + i));
}
