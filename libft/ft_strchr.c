#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	a;
	int		i;

	i = 0;
	a = (char)c;
	while ((char)*s != a)
	{
		if (*s == '\0')
			return (0);
		s++;
	}
	return ((char *)s);
}
